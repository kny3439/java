package day03;

public class SwitchEx {

	public static void main(String[] args) {

		int a = 1;

		switch (a) {
		case 1:
			System.out.println("1 인데용");
			break;
		case 2:
			System.out.println("2 인데용");
			break;
		case 3:
			System.out.println("3 인데용");
			break;
		case 4:
			System.out.println("4 인데용");
			break;

		default:
			System.out.println("1~4값이 아닙니다");
			break;
		}
	}
}
