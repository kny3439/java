package api.lang.math;

public class MathEx {

	public static void main(String[] args) {
		 
		// new Math();
		
		System.out.println(Math.random()); // 랜덤값
		
		System.out.println(Math.ceil(3.14)); // 올림
		
		System.out.println(Math.floor(3.14)); // 내림
		
		System.out.println(Math.round(3.14));  // 반올림(정수)
		
		System.out.println(Math.abs(-5)); // 절대값
		
		System.out.println(Math.max(8, 3)); // 큰 수
		
		System.out.println(Math.min(8, 3)); // 작은 수
		
		System.out.println(Math.sqrt(4)); //루트 값
		
	}
}
